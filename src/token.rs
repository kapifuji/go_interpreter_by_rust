#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Illegal,
    EndOfFile,
    Identifer(String),
    Integer(i32),
    Assign,
    Plus,
    Minus,
    Exclamation,
    Asterisk,
    Slash,
    LessThan,
    GraterThan,
    Equal,
    NotEqual,
    Comma,
    Semicolon,
    Lparentheses,
    Rparentheses,
    Lbrace,
    Rbrace,
    Function,
    Let,
    True,
    False,
    If,
    Else,
    Return,
}
